package dataTypes

fun main(){
    //Strings
    val str:String = "Hello World"
    println(str)
    println(str.uppercase())  //CAPITAL LETTERS
    println(str.lowercase())   //small letters
    println("=================")

    //Characters
    val aChar:Char = 'a'
    println(aChar)
    println('\n')     // new line
    println('\u2639')  //unicode
}